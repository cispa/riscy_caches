#!/usr/bin/env python3
from pathlib import Path
import re

DATA_DIR = Path("aes_t_table_results")


def discover_cpu_dirs():
    found = [p.name for p in DATA_DIR.iterdir() if p.is_dir()]
    found.sort()
    return [(name, name) for name in found]


def parse_runtime_log(path: Path):
    lines = [ln.strip() for ln in path.read_text(encoding="utf-8").splitlines() if ln.strip()]

    ratio_idx = []
    for i, ln in enumerate(lines):
        if re.fullmatch(r"\d+/\d+", ln):
            ratio_idx.append(i)

    if not ratio_idx:
        raise RuntimeError(f"No success ratio found in {path}")

    i = ratio_idx[-1]
    wins, runs = lines[i].split("/")
    success = float(wins) * 100.0 / float(runs)

    if i + 1 >= len(lines):
        raise RuntimeError(f"Missing runtime mean after ratio in {path}")
    mean_ms = float(lines[i + 1])

    stderr = 0.0
    if i + 2 < len(lines):
        m = re.fullmatch(r"stderr:\s*([0-9]+(?:\.[0-9]+)?)", lines[i + 2])
        if m:
            stderr = float(m.group(1))

    return success, mean_ms, stderr


def delta_success(fr: float, ours: float) -> str:
    d = ours - fr
    color = r"\good" if d >= 0 else r"\bad"
    return f"{color}{{{d:+.0f}}}"


def delta_time(fr: float, ours: float) -> str:
    if fr == 0:
        return r"???"
    d = (ours - fr) / fr * 100.0
    color = r"\good" if d <= 0 else r"\bad"
    return f"{color}{{{d:+.1f}}}"


def main():
    cpu_dirs = discover_cpu_dirs()
    if not cpu_dirs:
        raise RuntimeError(f"No per-CPU result directories found in {DATA_DIR}")

    rows = []
    for key, name in cpu_dirs:
        base = DATA_DIR / key
        missing = []
        if not (base / "runtime_fr.log").exists():
            missing.append("runtime_fr.log")
        if not (base / "runtime_archsc.log").exists():
            missing.append("runtime_archsc.log")
        if missing:
            raise RuntimeError(
                f"Missing required file(s) in {base}: {', '.join(missing)}"
            )
        fr_s, fr_t, fr_e = parse_runtime_log(base / "runtime_fr.log")
        our_s, our_t, our_e = parse_runtime_log(base / "runtime_archsc.log")
        rows.append((name, fr_s, our_s, fr_t, fr_e, our_t, our_e))

    latex = []
    latex.append(r"% Generated by aes_t_table/table.py")
    latex.append(r"\begin{tabular}{lrrrrrrrr}")
    latex.append(r"\toprule")
    latex.append(r"& \multicolumn{3}{c}{\textbf{Success Rate} (\si{\percent}, $\uparrow$)}")
    latex.append(r"& \multicolumn{5}{c}{\textbf{Time} (\si{\milli\second}, $\downarrow$)} \\")
    latex.append(r"\cmidrule(lr){2-4}\cmidrule(lr){5-9}")
    latex.append(r"\textbf{Microarch.}")
    latex.append(r"& \textbf{F+R} & \textbf{I2SC} & {$\Delta$}")
    latex.append(r"& \textbf{F+R} & ($\pm$, $n$=\num{100}) & \textbf{I2SC} & ($\pm$, $n$=\num{100}) & {$\Delta$\%} \\")
    latex.append(r"\midrule")

    for name, fr_s, our_s, fr_t, fr_e, our_t, our_e in rows:
        latex.append(
            f"{name} "
            f"& {fr_s:.0f} & {our_s:.0f} & {delta_success(fr_s, our_s)} "
            f"& {fr_t:.0f} & ({fr_e:.2f}) & {our_t:.0f} & ({our_e:.2f}) & {delta_time(fr_t, our_t)} \\\\" 
        )

    latex.append(r"\bottomrule")
    latex.append(r"\end{tabular}")

    out = "\n".join(latex)
    print(out)
    Path("table.tex").write_text(out + "\n", encoding="utf-8")


if __name__ == "__main__":
    main()
